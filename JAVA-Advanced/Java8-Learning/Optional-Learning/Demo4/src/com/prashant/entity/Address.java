package com.prashant.entity;

public class Address {
	private int id;
	private String city;

	public Address() {
		super();
		this.id = 100;
		this.city = "YouTube";
	}

	public Address(int id, String city) {
		super();
		this.id = id;
		this.city = city;
	}

	public int getId() {
		return id;
	}

	public String getCity() {
		return city;
	}

	@Override
	public String toString() {
		return "Address [id=" + id + ", city=" + city + "]";
	}
}